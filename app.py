import numpy as np
import joblib

# ===============================
# Load Files
# ===============================
model = joblib.load("best_model.pkl")
scaler = joblib.load("scaler.pkl")
encoders = joblib.load("label_encoder.pkl")

# ===============================
# Page Config
# ===============================
st.set_page_config(
    page_title="Student Depression Prediction",
    page_icon="🧠",
    layout="wide"
)

# ===============================
# CSS
