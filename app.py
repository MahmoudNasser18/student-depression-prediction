# ===============================
# Input Columns
# ===============================

left,right=st.columns(2)

# ===============================
# Inputs
# ===============================

with left:

    gender = st.selectbox(
        "Gender",
        encoders["Gender"].classes_
    )

    age = st.number_input(
        "Age",
        min_value=15,
        max_value=100,
        value=22
    )

    city = st.selectbox(
        "City",
        encoders["City"].classes_
    )

    profession = st.selectbox(
        "Profession",
        encoders["Profession"].classes_
    )

    academic_pressure = st.slider(
        "Academic Pressure",
        0,5,3
    )

    work_pressure = st.slider(
        "Work Pressure",
        0,5,0
    )

    cgpa = st.number_input(
        "CGPA",
        min_value=0.0,
        max_value=10.0,
        value=7.0
    )

    study_satisfaction = st.slider(
        "Study Satisfaction",
        0,5,3
    )

with right:

    job_satisfaction = st.slider(
        "Job Satisfaction",
        0,5,0
    )

    sleep = st.selectbox(
        "Sleep Duration",
        encoders["Sleep Duration"].classes_
    )

    dietary = st.selectbox(
        "Dietary Habits",
        encoders["Dietary Habits"].classes_
    )

    degree = st.selectbox(
        "Degree",
        encoders["Degree"].classes_
    )

    suicidal = st.selectbox(
        "Have you ever had suicidal thoughts ?",
        encoders["Have you ever had suicidal thoughts ?"].classes_
    )

    work_hours = st.slider(
        "Work/Study Hours",
        0,24,8
    )

    financial = st.slider(
        "Financial Stress",
        0,5,2
    )

    family = st.selectbox(
        "Family History of Mental Illness",
        encoders["Family History of Mental Illness"].classes_
    )

    # ===============================
# Predict Button
# ===============================

if st.button("🔍 Predict", use_container_width=True):

    # Encode categorical values
    gender = encoders["Gender"].transform([gender])[0]
    city = encoders["City"].transform([city])[0]
    profession = encoders["Profession"].transform([profession])[0]
    sleep = encoders["Sleep Duration"].transform([sleep])[0]
    dietary = encoders["Dietary Habits"].transform([dietary])[0]
    degree = encoders["Degree"].transform([degree])[0]
    suicidal = encoders["Have you ever had suicidal thoughts ?"].transform([suicidal])[0]
    family = encoders["Family History of Mental Illness"].transform([family])[0]

    # Create dataframe بنفس ترتيب التدريب
    input_data = pd.DataFrame([[
        gender,
        age,
        city,
        profession,
        academic_pressure,
        work_pressure,
        cgpa,
        study_satisfaction,
        job_satisfaction,
        sleep,
        dietary,
        degree,
        suicidal,
        work_hours,
        financial,
        family
    ]], columns=[
        "Gender",
        "Age",
        "City",
        "Profession",
        "Academic Pressure",
        "Work Pressure",
        "CGPA",
        "Study Satisfaction",
        "Job Satisfaction",
        "Sleep Duration",
        "Dietary Habits",
        "Degree",
        "Have you ever had suicidal thoughts ?",
        "Work/Study Hours",
        "Financial Stress",
        "Family History of Mental Illness"
    ])

    st.write("Input Data")
    st.dataframe(input_data)

    # Scale
    input_scaled = scaler.transform(input_data)

    # Prediction
    prediction = model.predict(input_scaled)[0]

    # Probability
    probability = model.predict_proba(input_scaled)[0]
    confidence = probability[prediction] * 100

    st.divider()

    st.subheader("Prediction Result")

    if prediction == 1:

        st.error("🔴 Depression Detected")

    else:

        st.success("🟢 No Depression Detected")

    st.progress(int(confidence))

    st.metric(
        label="Model Confidence",
        value=f"{confidence:.2f}%"
    )

    with st.expander("📋 Input Summary"):

        st.dataframe(input_data)